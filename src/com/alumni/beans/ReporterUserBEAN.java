package com.alumni.beans;

public class ReporterUserBEAN {

	// ================================================================
	private int reporter_id;
	private String issu_msg;
	private String reporter_fname;
	private String reporter_lname;
	// ================================================================

	public int getReporter_id() {
		return reporter_id;
	}

	public void setReporter_id(int reporter_id) {
		this.reporter_id = reporter_id;
	}

	public String getIssu_msg() {
		return issu_msg;
	}

	public void setIssu_msg(String issu_msg) {
		this.issu_msg = issu_msg;
	}

	public String getReporter_fname() {
		return reporter_fname;
	}

	public void setReporter_fname(String reporter_fname) {
		this.reporter_fname = reporter_fname;
	}

	public String getReporter_lname() {
		return reporter_lname;
	}

	public void setReporter_lname(String reporter_lname) {
		this.reporter_lname = reporter_lname;
	}

}
