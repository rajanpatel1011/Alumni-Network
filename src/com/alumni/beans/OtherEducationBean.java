package com.alumni.beans;

public class OtherEducationBean {

	private String course;
	private String degree;
	private String passoutYear;
	private String collegeName;
	private int otherEducationId;

	public int getOtherEducationId() {
		return otherEducationId;
	}

	public void setOtherEducationId(int otherEducationId) {
		this.otherEducationId = otherEducationId;
	}

	public String getCourse() {
		return course;
	}

	public void setCourse(String course) {
		this.course = course;
	}

	public String getDegree() {
		return degree;
	}

	public void setDegree(String degree) {
		this.degree = degree;
	}

	public String getPassoutYear() {
		return passoutYear;
	}

	public void setPassoutYear(String passoutYear) {
		this.passoutYear = passoutYear;
	}

	public String getCollegeName() {
		return collegeName;
	}

	public void setCollegeName(String collegeName) {
		this.collegeName = collegeName;
	}

}
